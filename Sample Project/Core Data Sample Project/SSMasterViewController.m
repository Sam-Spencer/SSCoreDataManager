- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath {
    NSManagedObject *object = [eventsList objectAtIndex:indexPath.row];
    cell.textLabel.text = [[object valueForKey:@"date"] description];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"Object ID: %@", [[object valueForKey:@"eventID"] description]];
}

@end
